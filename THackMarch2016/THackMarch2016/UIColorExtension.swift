import Foundation 

extension UIColor {
  static var th_blackColor: UIColor {
    return UIColor(hexString: "000000")
  }
  
  static var th_redColor: UIColor {
    return UIColor(hexString: "F12138")
  }
  
  static var th_blueColor: UIColor {
    return UIColor(hexString: "73ADF0")
  }
}